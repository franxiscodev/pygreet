"""
Módulo de operaciones matemáticas
"""


def sum(a: float, b: float) -> float:
    """
    Suma dos números

    Args:
        a (float): Primer número
        b (float): Segundo número

    Returns:
        float: Resultado de la suma
    """
    return a + b


def sub(a: float, b: float) -> float:
    """
    Resta dos números

    Args:
        a (float): Primer número
        b (float): Segundo número

    Returns:
        float: Resultado de la resta
    """
    return a - b
